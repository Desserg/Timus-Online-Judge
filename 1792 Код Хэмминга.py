H = list(map(int, input().split(' ')))

s = []
s.append((H[4] + H[1] + H[2] + H[3]) % 2) # h0 - i4   h3 - i2
s.append((H[5] + H[0] + H[2] + H[3]) % 2) # h1 - i1
s.append((H[6] + H[0] + H[1] + H[3]) % 2) # h2 - i3

if sum(s) == 1:
    H[4] = (H[4] + s[0]) %2
    H[5] = (H[5] + s[1]) %2
    H[6] = (H[6] + s[2]) %2
    
if s == [0,1,1]:
    H[0] = (H[0] + 1)%2
if s == [1,0,1]:
    H[1] = (H[1] + 1)%2
if s == [1,1,0]:
    H[2] = (H[2] + 1)%2
if s == [1,1,1]:
    H[3] = (H[3] + 1)%2

print(*H)

